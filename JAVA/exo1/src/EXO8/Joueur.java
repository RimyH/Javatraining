package EXO8;

public class Joueur {

    private String nom ;
    private int anneeDeNaissance;

    public Joueur(String nom, int anneeDeNaissance) {
        this.nom = nom;
        this.anneeDeNaissance = anneeDeNaissance;
    }

    public String getNom() {
        return nom;
    }

    public int getAnneeDeNaissance() {
        return anneeDeNaissance;
    }

    
    
}
